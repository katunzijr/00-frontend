function mergeRoutes(...routeClasses: any[]): Record<string, string> {
  const mergedRoutes: Record<string, string> = {};

  routeClasses.forEach(routeClass => {
    const properties = Object.getOwnPropertyNames(routeClass)
      .filter(prop => typeof Object.getOwnPropertyDescriptor(routeClass, prop)?.get === 'function'); // Only getters

    properties.forEach(prop => {
      mergedRoutes[prop] = (routeClass as any)[prop]; // Invoke getter
    });
  });

  return mergedRoutes;
}

